package Jeu;

/**
 * Enumération pour le choix du mode de jeu
 * 
 * @author Morgan
 *
 */
public enum Mode {
	/**
	 * L'attaquant est le joueur et l'ordinateur le défenseur
	 */
	Challenger,
	/**
	 * L'ordinateur est l'attaquant et le joueur le défenseur
	 */
	Defenseur,
	/**
	 * Le joueur et l'ordinateur s'affronte l'un contre l'autre
	 */
	Duel;

}
